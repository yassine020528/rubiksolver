def center_face_for_cubie(x: int, y: int, z: int) -> str | None:
    if x == 0 and y == 0 and z == 1:
        return "front"
    if x == 0 and y == 0 and z == -1:
        return "back"
    if x == 1 and y == 0 and z == 0:
        return "right"
    if x == -1 and y == 0 and z == 0:
        return "left"
    if y == 1 and x == 0 and z == 0:
        return "top"
    if y == -1 and x == 0 and z == 0:
        return "bottom"
    return None


def label_for_face(face: str) -> str:
    return FACE_LETTERS[face]
